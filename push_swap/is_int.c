#include "push_swap.h"

int			is_int(long long num)
{
	if (num < -2147483648)
	{
		ft_putendl_fd("Error", 2);
		exit(0);
	}
	else if (num > 2147483647)
	{
		ft_putendl_fd("Error", 2);
		exit(0);
	}
	return ((int)num);
}
