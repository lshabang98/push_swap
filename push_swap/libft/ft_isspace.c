#include "libft.h"

int		ft_isspace(int c)
{
	c = (unsigned char)c;
	if (c == ' ' || c == '\t' || c == '\v')
		return (1);
	else if (c == '\r' || c == '\f' || c == '\n')
		return (1);
	return (0);
}
