#include "libft.h"

void	ft_putendl(const char *s)
{
	int		i;
	char	endl;

	endl = '\n';
	i = 0;
	if (!s)
		return ;
	while (s[i] != '\0')
	{
		write(1, &s[i], 1);
		i++;
	}
	write(1, &endl, 1);
}
