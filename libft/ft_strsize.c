#include "libft.h"

int	ft_strsize(const char *s, char c)
{
	int	i;
	int k;

	i = 0;
	k = 0;
	while (s[i] != '\0')
	{
		while (s[i] != c)
		{
			if (s[i] == '\0')
				return (k);
			i++;
			k++;
		}
		i++;
	}
	return (k);
}
